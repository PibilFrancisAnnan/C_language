#include <stdio.h>
/*C identifiers represent the name in the C program, for example, variables, functions, arrays, structures, unions, labels, etc*/

int main()
{
    int Francis = 60; // The name Francis is an identifier
    int _Fustina = 40; // The name _Fustina is an identifier
    int love; // The word love is an identifier

    love = Francis + _Fustina;
    printf("The affection between Francis and Fustina is %d%%", love);

    return 0;
}